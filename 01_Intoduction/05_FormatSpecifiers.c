#include <stdio.h>

int main()
{
    int a_constant = 100;
    printf("Integer constant: %d\n", a_constant);

    char b_constant = 'a';
    printf("Char constant: %c\n", b_constant);

    float f_constant = 1.2;
    printf("Float constant: %f\n", f_constant);

    double d_constant = 10.23456;
    printf("Double constant: %lf\n", d_constant);
    return 0;
}